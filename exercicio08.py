# 8.Cálculo de Média Escolar: P1, P2 e P3 são as provas. A P1 e P2 tem
# peso de 40% da média final e a P3 60%. Exiba as notas e a média final

P1 = float(input("Insira sua nota da P1: "))
P2 = float(input("Insira sua nota da P2: "))
P3 = float(input("Insira sua nota da P3: "))
media_final = (P1 * 0.2) + (P2 * 0.2) + (P3 * 0.6)

print(f"Nota P1: {P1:.1f}, Nota P2: {P2:.1f}, Nota P3: {P3:.1f}")
print(f"Média final: {media_final:.1f}")

